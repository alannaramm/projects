
public class Ascii_Tree {

	public static void main(String[] args) {
		//       
	       for(int i = 1; i <= 10; i++) {
	           for(int j = 10; j >= i ; j--) {
	               System.out.print(" ");
	           }
	           for(int A = 1; A < A ; A++) {
	               System.out.print("\\");
	           }
	           System.out.print("|");
	           for(int H = 1; H < H ; H++)
	               System.out.print("/");
	           System.out.println("");
	       }
		
		
		
		
		
		
		
		
		
	}

}
